const supportedLanguages = Object.keys(translations);

/** 获取浏览器或本地存储中最合适的官网语言 */
function getInitialLanguage() {
  const savedLanguage = window.localStorage.getItem("photoCleanerSiteLanguage");
  if (supportedLanguages.includes(savedLanguage)) {
    return savedLanguage;
  }

  const browserLanguage = (window.navigator.language || "en").toLowerCase();
  if (browserLanguage.startsWith("zh")) return "zh";
  if (browserLanguage.startsWith("ja")) return "ja";
  if (browserLanguage.startsWith("ko")) return "ko";
  return "en";
}

/** 切换官网语言并同步可访问属性与按钮状态 */
function setLanguage(language) {
  const activeLanguage = supportedLanguages.includes(language) ? language : "en";
  const activeCopy = translations[activeLanguage];

  document.documentElement.lang = activeLanguage === "zh" ? "zh-Hans" : activeLanguage;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (activeCopy[key]) {
      node.textContent = activeCopy[key];
    }
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    const isActive = button.getAttribute("data-lang") === activeLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  window.localStorage.setItem("photoCleanerSiteLanguage", activeLanguage);
}

/** 初始化语言切换按钮 */
function initLanguageSwitcher() {
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.getAttribute("data-lang")));
  });
  setLanguage(getInitialLanguage());
}

/** 初始化滚动进入视口的轻量动效 */
function initRevealObserver() {
  const revealNodes = document.querySelectorAll(".reveal");
  if (!("IntersectionObserver" in window)) {
    revealNodes.forEach((node) => node.classList.add("is-visible"));
    return;
  }

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.14 });

  revealNodes.forEach((node) => observer.observe(node));
}

/** 初始化桌面端卡片倾斜反馈 */
function initTiltCards() {
  if (window.matchMedia("(pointer: coarse), (prefers-reduced-motion: reduce)").matches) {
    return;
  }

  document.querySelectorAll(".tilt-card").forEach((card) => {
    card.addEventListener("pointermove", (event) => {
      const rect = card.getBoundingClientRect();
      const x = (event.clientX - rect.left) / rect.width - 0.5;
      const y = (event.clientY - rect.top) / rect.height - 0.5;
      card.style.transform = `perspective(900px) rotateX(${y * -4}deg) rotateY(${x * 5}deg) translateY(-2px)`;
    });

    card.addEventListener("pointerleave", () => {
      card.style.transform = "";
    });
  });
}

/** 启动官网交互 */
function initSite() {
  initLanguageSwitcher();
  initRevealObserver();
  initTiltCards();
}

initSite();
